CREATE TABLE REQUESTS(
    REQUEST_ID INTEGER PRIMARY KEY,
    EMPLOYEE_ID NUMBER,
    REQUEST DATE NOT NULL,
    REQUEST_STATUS VARCHAR2(100) NOT NULL,
    RECEIPT DATE NOT NULL,
    RECEIPT_IMG BLOB,
    REQUEST_AMT NUMBER(10,2) 
);
/



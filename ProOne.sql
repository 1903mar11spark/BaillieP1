    EMPLOYEE_ID NUMBER PRIMARY KEY,
    PRO_PIC BLOB,
    FIRSTNAME VARCHAR2(100),
    LASTNAME VARCHAR2(100),
    EMAIL VARCHAR2(100) NOT NULL,
    TITLE VARCHAR2(100) DEFAULT 'BARISTA',
    USERNAME VARCHAR2(100) NOT NULL,
    UPASSWORD VARCHAR2(100) NOT NULL,
    MANAGEDBY NUMBER
    );
